function buildHandoffData(memory: ProjectMemory, delta: any): Record<string, unknown> {
  const recentSessions = memory.sessions
    .slice(-5)
    .reverse();

  const recentCommits = memory.evidence.git?.recentCommits?.slice(0, 10) || [];

  // Identify key files from entry points + hotspots
  const keyFiles = new Set<string>();
  for (const ep of memory.evidence.sourceCode.entryPoints) {
    keyFiles.add(ep);
  }
  if (memory.evidence.git?.hotspots) {
    for (const hs of memory.evidence.git.hotspots.slice(0, 5)) {
      keyFiles.add(hs.filePath);
    }
  }

  let focusProgress = { completed: 0, total: 0, percentage: 0 };
  let scopeDrift = { hasDrift: false, extraModules: [] as string[] };
  
  if (memory.focusHistory.active) {
    focusProgress = computeProgress(memory);
    scopeDrift = detectScopeDrift(memory);
  }

  return {
    projectName: memory.projectName,
    version: memory.version,
    description: memory.description,
    scenario: formatScenario(memory.scenario),
    createdAt: memory.createdAt,
    updatedAt: memory.updatedAt,
    languages: memory.evidence.sourceCode.languages,
    frameworks: memory.evidence.buildFiles.frameworks,
    architecture: memory.architecture,
    workflows: memory.workflows?.slice(0, 5) || [],
    features: memory.features?.slice(0, 10) || [],
    dependencies: buildDependenciesList(memory.evidence.buildFiles.dependencies),
    inventory: buildInventoryList(memory.architecture.components),
    timeline: memory.timeline?.slice(-5).reverse() || [],
    recentCommits,
    recentSessions,
    decisions: memory.decisions,
    focusHistory: memory.focusHistory,
    focusProgress,
    scopeDrift,
    confidence: memory.confidence,
    keyFiles: Array.from(keyFiles),
    sourceFiles: memory.evidence.sourceCode.totalFiles,
    totalLines: memory.evidence.sourceCode.totalLines.toLocaleString(),
    entryPoints: memory.evidence.sourceCode.entryPoints,
    hasGit: memory.evidence.git?.isGitRepo ?? false,
    gitCommits: memory.evidence.git?.totalCommits ?? 0,
    gitAuthors: memory.evidence.git?.authors.length ?? 0,
    buildSystems: memory.evidence.buildFiles.buildSystems.map(b => b.name).join(', ') || 'none',
    hasReadme: memory.evidence.documentation.hasReadme,
    license: memory.evidence.documentation.licenseType || 'not detected',
    delta,
  };
}

function formatScenario(scenario: string): string {
  switch (scenario) {
    case 'brand-new': return 'Brand New Project';
    case 'existing': return 'Existing Project (no Git)';
    case 'mature': return 'Mature Project (with Git)';
    case 'fully-tracked': return 'Fully Tracked Project';
    default: return scenario;
  }
}

function buildDependenciesList(deps: any[]): any[] {
  if (!deps) return [];
  // Filter out some internal noise if needed, but for now just return top 30
  // Sort by name for stability
  return [...deps]
    .filter(d => d.type === 'production')
    .sort((a, b) => a.name.localeCompare(b.name))
    .slice(0, 30);
}

function buildInventoryList(components: any[]): any[] {
  if (!components) return [];
  const inventoryMap = new Map<string, string[]>();

  for (const comp of components) {
    if (comp.files && comp.files.length > 0) {
      const layer = comp.type.charAt(0).toUpperCase() + comp.type.slice(1) + 's';
      const existing = inventoryMap.get(layer) || [];
      
      let item = comp.files[0];
      if (comp.endpoints && comp.endpoints.length > 0) {
        item += ` ⚡ [${comp.endpoints.join(', ')}]`;
      }
      
      inventoryMap.set(layer, [...existing, item]);
    }
  }

  const inventory = [];
  for (const [layer, files] of inventoryMap.entries()) {
    inventory.push({
      layer,
      files: files.slice(0, 15) // cap at 15 files per type to save tokens
    });
  }
  
  return inventory.sort((a, b) => a.layer.localeCompare(b.layer));
}

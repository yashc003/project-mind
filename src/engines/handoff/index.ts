/**
 * Generates all handoff documents from the current memory state.
 */
export async function generateHandoff(
  projectPath: string,
  memory: ProjectMemory,
): Promise<void> {
  const paths = getMemoryFilePaths(projectPath);
  let memoryPrev: ProjectMemory | null = null;

  if (await fileExists(paths.memoryPrev)) {
    try {
      const prevStr = await fs.readFile(paths.memoryPrev, 'utf-8');
      memoryPrev = JSON.parse(prevStr) as ProjectMemory;
    } catch (err) {
      // Ignore
    }
  }

  const delta = compareMemory(memoryPrev, memory);
  const data = buildHandoffData(memory, delta);

  // Compile templates
  const aiStartHere = Handlebars.compile(AI_START_HERE_TEMPLATE)(data);
  const projectContext = Handlebars.compile(PROJECT_CONTEXT_TEMPLATE)(data);
  const handoff = Handlebars.compile(HANDOFF_TEMPLATE)(data);

  // Write all handoff files
  await Promise.all([
    writeText(paths.aiStartHere, aiStartHere),
    writeText(paths.projectContext, projectContext),
    writeText(paths.handoff, handoff),
  ]);

  logger.success(`Generated AI_START_HERE.md`);
  logger.success(`Generated PROJECT_CONTEXT.md`);
  logger.success(`Generated HANDOFF.md`);
}


Handlebars.registerHelper('date', (isoString: string) => {
  if (!isoString) return 'N/A';
  return new Date(isoString).toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
  });
});

Handlebars.registerHelper('datetime', (isoString: string) => {
  if (!isoString) return 'N/A';
  return new Date(isoString).toLocaleString('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  });
});

Handlebars.registerHelper('pct', (value: number) => {
  return `${value}%`;
});

Handlebars.registerHelper('confidenceEmoji', (score: number) => {
  if (score >= 80) return '🟢';
  if (score >= 50) return '🟡';
  return '🔴';
});

Handlebars.registerHelper('eq', (a: unknown, b: unknown) => a === b);
Handlebars.registerHelper('gt', (a: number, b: number) => a > b);
Handlebars.registerHelper('ifCond', function (this: unknown, v1: unknown, v2: unknown, options: Handlebars.HelperOptions) {
  if (v1 === v2) return options.fn(this);
  return options.inverse(this);
});

